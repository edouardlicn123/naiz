;#---------------------------------------------------------------
;# function lz4_decompress
;#
;# Decompresses an LZ4 stream file with a compressed chunk 64K or less in size.
;# Input:
;#   DS:SI Location of source data.  4-byte compressed chunk size must be intact
;#         at the start of the data (written by C wrapper).
;#
;# Output:
;#   ES:DI Decompressed data.
;#   AX    Size of decompressed data.
;#
;# Trashes AX, BX, CX, DX, SI, DI
;#---------------------------------------------------------------
.p2align 2
.globl	lz4_decompress_asm
lz4_decompress_asm:
        push    ds
        push    di
        cld
        mov     bx,offset SHR4table
        lodsw
        mov     bp,ax
        lodsw
        add     bp,si
        or      ax,ax
        jnz     done_1

starttoken:
        lodsb
        mov     dx,ax
        cs xlat
        mov     cx,ax
        jcxz    copymatches_1
        cmp     al,0x0F
        jne     doliteralcopy1
build1stcount:
        lodsb
        add     cx,ax
        cmp     al,0xFF
        je      build1stcount
doliteralcopy1:
        rep     movsb

        cmp     si,bp
        movw    [cs:end_of_chunk_1+2],bp
        mov     bp,ds
        jae     done_1

copymatches_1:
        lodsw
        xchg    dx,ax
        and     al,0x0F
        cmp     al,0x0F
        xchg    cx,ax
        je      buildmcount

domatchcopy_1:
        cmp     dx,2
        jbe     domatchfill
        xchg    si,ax
        mov     si,di
        sub     si,dx
        mov     dx,es
        mov     ds,dx
        movsw
        shr     cx,1
        jnc     even_1
        movsb
even_1:
        movsw
		rep     movsw
        xchg    si,ax
        mov     ds,bp

parsetoken_1:
        xchg    cx,ax
        lodsb
        mov     dx,ax
copyliterals_1:
        cs xlat
        mov     cx,ax
        jcxz    copymatches_1
        cmp     al,0x0F
        je      buildlcount
doliteralcopy_1:
        rep     movsb

testformore:
end_of_chunk_1:
		cmp     si,256
        jb      copymatches_1
        jmp     done_1

domatchfill:
        je      domatchfill2
domatchfill1:
        mov     al,es:[di-1]
        mov     ah,al
        stosw
        stosw
        inc     cx
        shr     cx,1
        rep     stosw
        adc     di,-1
        jmp     parsetoken_1

domatchfill2:
        mov     ax,es:[di-2]
        stosw
        stosw
        inc     cx
        shr     cx,1
        rep     stosw
        adc     di,-1
        jmp     parsetoken_1

buildlcount:
        lodsb
        add     cx,ax
        cmp     al,0xFF
        je      buildlcount
        jmp     doliteralcopy_1

buildmcount:
        lodsb
        add     cx,ax
        cmp     al,0xFF
        je      buildmcount
        jmp     domatchcopy_1

done_1:
        pop     ax
        sub     di,ax
        xchg    ax,di
        pop     ds
        ret
