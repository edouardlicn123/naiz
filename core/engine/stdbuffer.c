short sin_fixed(unsigned int x)
{
    unsigned short idx = x >> 6;
    short val = sine_table[idx & 0xFF];
    if (idx & 0x100) val = -val;
    return val;
}

short cos_fixed(unsigned int x)
{
    return sin_fixed(x + 0x2000);
}

unsigned int atan2_fixed(short y, short x)
{
    if (y == 0)
    {
        if (x >= 0) return 0;
        else return 16384;
    }
    if (x == 0)
    {
        if (y > 0) return 8192;
        else return 24576;
    }
    unsigned short uy, ux;
    unsigned int quad;
    if (y > 0)
    {
        uy = y;
        if (x > 0) { quad = 0; ux = x; }
        else { quad = 8192; ux = (x == -0x8000) ? 0x8000 : -x; }
    }
    else
    {
        uy = (y == -0x8000) ? 0x8000 : -y;
        if (x > 0) { quad = 24576; ux = x; }
        else { quad = 16384; ux = (x == -0x8000) ? 0x8000 : -x; }
    }
    unsigned char msby = 0, msbx = 0;
    unsigned short checker = 0x8000;
    for (int i = 15; i >= 0; i--)
    {
        if (uy & checker) { msby = i; break; }
        checker >>= 1;
    }
    checker = 0x8000;
    for (int i = 15; i >= 0; i--)
    {
        if (ux & checker) { msbx = i; break; }
        checker >>= 1;
    }
    unsigned char msb = (msby > msbx) ? msby : msbx;
    unsigned short iy = uy, ix = ux;
    if (msb > 4)
    {
        iy = uy >> (msb - 4);
        ix = ux >> (msb - 4);
    }
    if (iy > 16) iy >>= 1;
    if (ix > 16) ix >>= 1;
    if (quad & 0x2000)
    {
        unsigned short t = iy; iy = ix; ix = t;
    }
    return (unsigned int)atan2_table[(iy-1)*16 + (ix-1)] + quad;
}

unsigned short sqrt_fixed(unsigned long x)
{
    if (x < 256) return sqrt_table[(unsigned short)x];
    unsigned long x0;
    if (x < 65536) x0 = 16 * ((unsigned long)sqrt_table[(unsigned short)(x >> 8)] + 1);
    else if (x < 16777216) x0 = 256 * ((unsigned long)sqrt_table[(unsigned short)(x >> 16)] + 1);
    else x0 = 4096 * ((unsigned long)sqrt_table[(unsigned short)(x >> 24)] + 1);
    unsigned long x1 = (x0 + (x / x0)) / 2;
    while (x1 < x0) { x0 = x1; x1 = (x0 + (x / x0)) / 2; }
    return (unsigned short)x0;
}

void *memcpy(void *dst, const void *src, unsigned int n)
{
    const unsigned char *s = src;
    unsigned char *d = dst;
    for (unsigned int i = 0; i < n; i++) d[i] = s[i];
    return dst;
}

unsigned long __ashlsi3(unsigned long a, unsigned int b)
{
    if (b >= 32) return 0;
    return a << b;
}

unsigned long __lshrsi3(unsigned long a, unsigned int b)
{
    if (b >= 32) return 0;
    return a >> b;
}

long __ashrsi3(long a, unsigned int b)
{
    if (b >= 32) return a < 0 ? -1L : 0;
    return a >> b;
}

unsigned long __udivsi3(unsigned long a, unsigned long b)
{
    unsigned long q = 0, r = 0;
    for (int i = 31; i >= 0; i--)
    {
        r = (r << 1) | ((a >> i) & 1);
        if (r >= b) { r -= b; q |= (1UL << i); }
    }
    return q;
}

long __divsi3(long a, long b)
{
    int neg = 0;
    if (a < 0) { a = -a; neg = 1; }
    if (b < 0) { b = -b; neg = !neg; }
    long q = (long)__udivsi3((unsigned long)a, (unsigned long)b);
    return neg ? -q : q;
}
